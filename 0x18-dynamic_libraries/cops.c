#include <stdio.h>

/**
 * add - add two numbers
 * @a: first number
 * @b: second number
 * Return: always interger
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * mul - multiplies two numbers
 * @a: first number
 * @b: second number
 * Return: always interger
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * sub - subtract two numbers
 * @a: first number
 * @b: second number
 * Return: always interger
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * div - division of 2 numbers
 * @a: first int
 * @b: second int
 * Return: result of the division of the numbers
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - find mode of a numbers
 * @a: first number
 * @b: second number
 * Return: always interger
 */

int mod(int a, int b)
{
	return (a % b);
}
